/**
 * @typedef {"left" | "right" | "center"} TextAlign
 */

/** @type {TextAlign} */
const left = "left";

/** @type {TextAlign} */
const right = "right";

/** @type {TextAlign} */
const center = "center";

const TextAlign = Object.freeze({
  left,
  right,
  center,
});

export default TextAlign;
